package com.javaex.practice;

public class Ex15 {
	public static void main(String[] args) {
		
		double mile;
		mile = 1.609;
		
		int x;
		x = 10;
		
		int y;
		y = 15;
		
		double km1;
		km1 = mile * x;
		
		double km2;
		km2 = mile * y;
		
		System.out.println("마일을 입력하세요:" + x);
		System.out.println("10.0마일은" + km1);
		
		System.out.println("마일을 입력하세요:" + y);
		System.out.println("15.0마일은" + km2);
		
	}

}
