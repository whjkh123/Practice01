package com.javaex.practice;

public class Ex04 {

	public static void main(String[] args) {
		int x, y;//문장의 끝은 마침표 ';'를 사용해야한다.
		
		x = 10;//문장의 끝은 마침표 ';'를 사용해야한다.
		y = 20;//문장의 끝은 마침표 ';'를 사용해야한다.
		
		int sum = x + y;//문장의 끝은 마침표 ';'를 사용해야한다.
		                //x와 y값 모두 정수이므로 int형을 정의하고 변수명 sum을 입력해야한다.
		
		System.out.println("합은 " + sum);

	}

}
